// Share functionality
function shareConference() {
    if (navigator.share) {
        navigator.share({
            title: 'ICAMM 2025 - International Conference on Applied Mathematical Models',
            text: 'Join us at ICAMM 2025, the Sixth International Conference on Applied Mathematical Models at PSG College of Technology, Coimbatore.',
            url: window.location.href
        });
    } else {
        // Fallback for browsers that don't support Web Share API
        const url = window.location.href;
        navigator.clipboard.writeText(url).then(() => {
            alert('Conference URL copied to clipboard!');
        });
    }
}
